function formatLogDetails(log: CareLog, L: Record<string, string>): string {
  const data = log.data as any;
  switch (log.logType) {
    case 'bowel':
      return `${data.type || ''} / ${data.location || ''}${data.isAccident ? ' ⚠' : ''}`;
    case 'urination':
      return `${data.method || ''} / ${data.volume || ''}${data.isIncontinence ? ' ⚠' : ''}`;
    case 'meal':
      if (data.mealType === 'fluid') return `${data.fluidAmountMl || 0}ml`;
      return `${data.mealType || ''}${data.description ? ' — ' + escapeHtml(data.description) : ''}`;
    case 'medication':
      return `${escapeHtml(data.medicationName || '')} / ${data.status || ''}`;
    case 'mood':
      return data.mood || '';
    case 'hygiene':
      return data.type || '';
    case 'activity':
      return `${data.activityType || ''}${
        data.durationMinutes ? ' (' + data.durationMinutes + 'min)' : ''
      }`;
    case 'note':
      return escapeHtml(data.content || '');
    default:
      return '';
  }
}

function escapeHtml(text: string): string {
  return text
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}
