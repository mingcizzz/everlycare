export const reportService = {
  async generateAndSharePdf(options: ReportOptions): Promise<void> {
    const { recipient, days, language } = options;
    const L = LABELS[language];

    // Gather data across the period
    const allLogs: CareLog[] = [];
    let totalFluidMl = 0;
    let bathroomCount = 0;
    let incontinenceCount = 0;

    const endDate = new Date();
    const startDate = new Date();
    startDate.setDate(startDate.getDate() - days + 1);

    for (let i = 0; i < days; i++) {
      const d = new Date(startDate);
      d.setDate(d.getDate() + i);
      const dateStr = d.toISOString().split('T')[0];
      const logs = await careLogService.getLogs(recipient.id, { date: dateStr });
      allLogs.push(...logs);
    }

    // Calculate summary
    for (const log of allLogs) {
      const data = log.data as any;
      switch (log.logType) {
        case 'bowel':
          bathroomCount++;
          break;
        case 'urination':
          bathroomCount++;
          if (data.isIncontinence) incontinenceCount++;
          break;
        case 'meal':
          if (data.mealType === 'fluid') {
            totalFluidMl += data.fluidAmountMl || 0;
          }
          break;
      }
    }

    const insights = await insightsService.getInsights(recipient.id, days);

    const periodStart = startDate.toLocaleDateString(language);
    const periodEnd = endDate.toLocaleDateString(language);
    const generatedAt = new Date().toLocaleString(language);

    // Sort logs by time descending
    const sortedLogs = [...allLogs].sort(
      (a, b) => new Date(b.occurredAt).getTime() - new Date(a.occurredAt).getTime()
    );

    const html = buildHtml({
      L,
      recipient,
      periodStart,
      periodEnd,
      days,
      generatedAt,
      totalLogs: allLogs.length,
      bathroomCount,
      totalFluidMl,
      incontinenceCount,
      medicationAdherence: insights.medicationAdherence,
      logs: sortedLogs,
      language,
    });

    const { uri } = await Print.printToFileAsync({ html, base64: false });

    if (await Sharing.isAvailableAsync()) {
      await Sharing.shareAsync(uri, {
        mimeType: 'application/pdf',
        dialogTitle: L.title,
        UTI: 'com.adobe.pdf',
      });
    }
  },
};

